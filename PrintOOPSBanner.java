/**
 * PrintOOPSBanner
 * Displays OOPS in banner format using * characters.
 *
 * @author Aishwarya Negi
 * @version 1.0
 */
public class PrintOOPSBanner {

    public static void main(String[] args) {

        System.out.println("  ***     " + "  ***     " + " *****   " + " ***** ");
        System.out.println(" ** **    " + " ** **    " + " **  **  " + " **    ");
        System.out.println("**   **   " + "**   **   " + " **   ** " + " **    ");
        System.out.println("**   **   " + "**   **   " + " *****   " + " ***   ");
        System.out.println("**   **   " + "**   **   " + " **      " + "   **  ");
        System.out.println(" ** **    " + " ** **    " + " **      " + "   **  ");
        System.out.println("  ***     " + "  ***     " + " **      " + " ***** ");
    }
}

